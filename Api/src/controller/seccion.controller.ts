import { Request, Response } from 'express';
import MySQL from "./../mysql/mysql";

export default class SeccionController {
    private static _instance: SeccionController;

    constructor() {
    }

    public static getInstance() {
        return this._instance || ( this._instance = new this() );
    }

    getAll = (req: Request, res: Response) => {
        const query = `
            SELECT * FROM Seccion
        `;

        MySQL.getQuery(query, (err:any, data:Object[]) => {
            if(err) {
                res.json([]);
            } else {
                res.json(data)
            }
        })
    }

    getSingle = (req: Request, res: Response) => {
        const query = `
            SELECT * FROM Seccion WHERE idSeccion = ?
        `;

        let body = {
            idSeccion : req.params.id
        }

        MySQL.sendQuery(query, body.idSeccion, (err:any, data:Object[]) => {
            if(err) {
                res.status(400).json({
                    ok: false,
                    status: 400,
                    error: err
                });
            } else {
                res.json(data[0])
            }
        })
    }
}